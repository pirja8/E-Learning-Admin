    private void resetForm() {
        txtidjadwal.setText("");
        txtnama.setText("");
        cbjurusan.setSelectedItem(0);
        cbkelas.setSelectedItem(0);
        cbmapel.setSelectedItem(0);
        txtjam.setText("");
    }

    private void setTabelModel() {
        DefaultTableModel model = (DefaultTableModel) tbldatajadwal.getModel();
        model.addColumn("Id Jadwal");
        model.addColumn("Nama Guru");
        model.addColumn("Mata Pelajaran");
        model.addColumn("Jurusan");
        model.addColumn("Kelas");
        model.addColumn("Hari");
        model.addColumn("Jam");
    }
    
    private void getData(DefaultTableModel model){
        model.setRowCount(0);
        
        try {
            String sql = "Select *from Jadwal";
            try (PreparedStatement st = conn.prepareStatement(sql)){
                ResultSet rs = st.executeQuery();
                
                while (rs.next()){
                    String idjadwal = rs.getString("Id_Jadwal");
                    String namaguru = rs.getString("Nama_Guru");
                    String namamapel = rs.getString("Nama_Mapel");
                    String jurusan = rs.getString("Jurusan");
                    String kelas = rs.getString("Kelas");
                    String hari = rs.getString("Hari");
                    String waktu = rs.getString("Jam");
                    
                    Object[] rowData = {idjadwal,namaguru,namamapel,jurusan,kelas,hari,waktu};
                    model.addRow(rowData);
                }
            }
        }catch (Exception e) {
            Logger.getLogger(DataJadwal.class.getName()).log(Level.SEVERE,null, e);
        }
    }

    private void insertData() {
        String idjadwal = txtidjadwal.getText();
        String namaguru = txtnama.getText();
        String mapel = cbmapel.getSelectedItem().toString();
        String jurusan = cbjurusan.getSelectedItem().toString();
        String kelas = cbkelas.getSelectedItem().toString();
        String hari = cbhari.getSelectedItem().toString();
        String waktu = txtjam.getText();
        
        if(idjadwal.isEmpty()||namaguru.isEmpty()||mapel.isEmpty()||jurusan.isEmpty()||kelas.isEmpty()||hari.isEmpty()||waktu.isEmpty()){
            JOptionPane.showMessageDialog(this,"Semua kolom harus diisi !","Validasi",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            String sql = "insert into jadwal(Id_Jadwal,Nama_Guru,Nama_Mapel,Jenis_Kelamin,Jurusan,Kelas,Hari,Jam) values (?,?,?,?,?,?,?)";
            try(PreparedStatement st = conn.prepareStatement(sql)){
                st.setString(1, idjadwal);
                st.setString(2, namaguru);
                st.setString(3, mapel);
                st.setString(4, jurusan);
                st.setString(5, kelas);
                st.setString(6, hari);
                st.setString(7, waktu);
                
                int rowInserted = st.executeUpdate();
                if(rowInserted > 0) {
                    JOptionPane.showMessageDialog(this,"Data Berhasil Ditambahkan");
                    resetForm();
                    loadData();
                    showPanel();
                }
            }
        } catch (Exception e){
            Logger.getLogger(DataJadwal.class.getName()).log(Level.SEVERE,null, e);
        }
    }

