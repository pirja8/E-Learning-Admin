    private void updateData() {
        String idjadwal = txtidjadwal.getText();
        String namaguru = txtnama.getText();
        String mapel = cbmapel.getSelectedItem().toString();
        String jurusan = cbjurusan.getSelectedItem().toString();
        String kelas = cbkelas.getSelectedItem().toString();
        String hari = cbhari.getSelectedItem().toString();
        String waktu = txtjam.getText();
        
        if(idjadwal.isEmpty()||namaguru.isEmpty()||mapel.isEmpty()||jurusan.isEmpty()||kelas.isEmpty()||hari.isEmpty()||waktu.isEmpty()){
            JOptionPane.showMessageDialog(this,"Semua kolom harus diisi !","Validasi",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            String sql = "update jadwal set Nama_Guru = ?,Nama_Mapel = ?,Jurusan = ?,Kelas = ? ,Hari = ?,Jam = ? where Id_Jadwal = ?";
            try(PreparedStatement st = conn.prepareStatement(sql)){
                st.setString(1, namaguru);
                st.setString(2, mapel);
                st.setString(3, jurusan);
                st.setString(4, kelas);
                st.setString(5, hari);
                st.setString(6, waktu);
                st.setString(7, idjadwal);
                
                int rowUpdated = st.executeUpdate();
                if(rowUpdated > 0) {
                    JOptionPane.showMessageDialog(this,"Data Berhasil Diperbarui");
                    resetForm();
                    loadData();
                    showPanel();
                }
            }
        } catch (Exception e){
            Logger.getLogger(DataJadwal.class.getName()).log(Level.SEVERE,null, e);
        }
    }

    private void deleteData() {
        int selectedRow = tbldatajadwal.getSelectedRow();
        int confirm = JOptionPane.showConfirmDialog(this,
                "Apakah Anda Yakin Ingin Menghapus Data Ini?",
                "Konfirmasi Hapus Data",
                JOptionPane.YES_NO_OPTION);
        
        if(confirm == JOptionPane.YES_OPTION){
            String nis = tbldatajadwal.getValueAt(selectedRow, 0).toString();
            try {
                String sql = "delete from Jadwal where Id_Jadwal=?";
                try(PreparedStatement st = conn.prepareStatement(sql)){
                    st.setString(1, nis);
                    int rowDeleted = st.executeUpdate();
                    if(rowDeleted > 0) {
                        JOptionPane.showMessageDialog(this,"Data Berhasil Dihapus");
                    } else{
                        JOptionPane.showMessageDialog(this,"Data Gagal Dihapus");
                    }
                } 
            } catch (Exception e){
            Logger.getLogger(DataJadwal.class.getName()).log(Level.SEVERE,null, e);
            }
        }
        resetForm();
        loadData();
        showPanel();
    }

